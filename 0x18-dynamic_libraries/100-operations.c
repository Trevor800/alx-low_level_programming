#include "main.h"

/**
 * add - function to add
 * @a: first arg
 * @b: second
 * Return: a,b
 */

int add(int a, int b) {
    return a + b;
}

/**
 * sub - function to sub
 * @a: first arg
 * @b: second a
 * Return: a,b
 */

int sub(int a, int b) {
    return a - b;
}

/**
 * mul - function to multiply
 * @a: first arg
 * @b: second a
 */

int mul(int a, int b) {
    return a * b;
}

/**
 * div - function to divide
 * @a: first arg
 * @b: second a
 * Return:a,b
 */
int div(int a, int b) {
    return a / b;
}

/**
 * mod - function to modulus
 * @a: first arg
 * @b: secondar
 * Return:a,b
 */
int mod(int a, int b) {
    return a % b;
}
